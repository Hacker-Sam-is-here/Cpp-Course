#include <iostream>


int main() {
    //Arithmetic Operators
    // operations = +, -, *, /, %

    //addition
    int students = 20;
    students = students + 2;
    students += 2; // same as students = students + 2
    students ++; // same as students = students + 1

    //subtraction
    students = students - 2;
    students -= 2; // same as students = students - 2
    students --; // same as students = students - 1


    return 0;
}